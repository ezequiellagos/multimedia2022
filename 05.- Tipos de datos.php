<?php

// Tipos de datos Numéricos
//enteros o integer
$numero_entero = 10;

// flotantes o float
$numero_flotante = 10.5;

//Dobles o double (más grande que un float)
$numero_doble = 10.5;

// Cadenas de caracteres
// Char
$letra = "a";

$cadena_caracteres = "Juan";
$otra_cadena_caracteres = 'Juan';

//Booleanos
$verdadero = true;
$false = false;

// Sin valor
// Null
$nulo = null;

// Undefined
// variable no está definida
echo $problema;

